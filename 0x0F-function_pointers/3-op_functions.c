#include "3-calc.h"

/**
 * op_add - calculate the sum of a and b
 * @a: variable int
 * @b: variable int
 *
 * Return: returns the sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculate the difference of a and b
 * @a: variable int
 * @b: variable int
 *
 * Return: returns the difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculate the product of a and b
 * @a: variable int
 * @b: variable int
 *
 * Return: returns the product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculate the division of a by b
 * @a: variable int
 * @b: variable int
 *
 * Return: returns the result of the division of a by b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculate the remainder of the division of a by b
 * @a: variable int
 * @b: variable int
 *
 * Return: returns the remainder of the division of a by b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
